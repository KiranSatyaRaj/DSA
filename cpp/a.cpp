#include <bits/stdc++.h>

using namespace std;

int main() {
	// for fast input and output use the below 3 lines
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	
	cout << "Hello, world!\n";
	string str;
	cin >> str;
	cout << str << '\n';
	cin.ignore(numeric_limits<streamsize>::max(), '\n');
	getline(cin, str);
	cout << str << '\n';
	return 0;
}
